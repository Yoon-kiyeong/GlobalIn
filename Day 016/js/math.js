//최대값 (Math.max)
let max = Math.max(100, 123);
document.write("<h3> 최댓값 : " + max + "</h3>");

//최소값 (Math.min)
let min = Math.min(100, 123);
document.write("<h3> 최소값 : " + min + "</h3>");

//원주율 (Math.PI)
document.write("<h3> 원주율 : " + Math.PI + "</h3>");

//소수점 반올림 (Math.round)
let num1 = 3.7146;
document.write("<h3> 소수점 반올림 : " + Math.round(num1) + "</h3>");

//소수점 올림과 내림 (Math.ceil, Math.floor)
document.write("<h3> 소수점 올림 : " + Math.ceil(num1) + "</h3>");
document.write("<h3> 소수점 내림 : " + Math.floor(num1) + "</h3>");

//절댓값 반환 (Math.max)
let num2 = -1.23;
document.write("<h3> 절댓값 변환 : " + Math.abs(num2) + "</h3>");

//난수발생 (Math.random)
//랜덤함수의 범위 : 0.0 ~ 1.0 사이의 값을 리턴함
document.write("<h3>난수 : " + Math.random() + "</h3>");





















